async function findUserByPhone(phone: string) {
  console.log('üîç Buscando usu√°rio pelo telefone:', phone);

  const cleanPhone = phone.replace(/\D/g, '');
  console.log('üì± Telefone limpo:', cleanPhone);

  // Primeiro buscar na tabela profiles
  const { data: profiles, error: profilesError } = await supabase
    .from('profiles')
    .select('user_id, name, phone')
    .or(`phone.eq.${phone},phone.eq.${cleanPhone}`)
    .limit(1);

  console.log('üîç Resultado da busca na tabela profiles:', { profiles, profilesError });

  if (profiles && profiles.length > 0) {
    console.log('‚úÖ Usu√°rio encontrado na tabela profiles:', profiles[0]);
    return {
      user_id: profiles[0].user_id,
      name: profiles[0].name || 'Usu√°rio',
      phone: profiles[0].phone
    };
  }

  // Se n√£o encontrar em profiles, buscar em companies como fallback
  const { data: companies, error: companiesError } = await supabase
    .from('companies')
    .select('user_id, name, email, phone, country_code')
    .or(`phone.eq.${phone},phone.eq.${cleanPhone}`)
    .limit(1);

  console.log('üîç Resultado da busca na tabela companies:', { companies, companiesError });

  if (companies && companies.length > 0) {
    console.log('‚úÖ Usu√°rio encontrado na tabela companies:', companies[0]);
    return {
      user_id: companies[0].user_id,
      name: companies[0].name || 'Usu√°rio',
      phone: companies[0].phone,
      email: companies[0].email
    };
  }

  console.log('‚ùå Usu√°rio n√£o encontrado pelo telefone');
  return null;
}

async function getRecentProposals(userId: string) {
  try {
    const { data: proposals, error } = await supabase
      .from('proposals')
      .select(`
        id,
        title,
        status,
        value,
        created_at,
        companies (name)
      `)
      .eq('user_id', userId)
      .order('created_at', { ascending: false })
      .limit(10);

    if (error) {
      console.error('‚ùå Erro ao buscar propostas:', error);
      return [];
    }

    return proposals || [];
  } catch (error) {
    console.error('‚ùå Erro ao buscar propostas:', error);
    return [];
  }
}

async function createProposalForUser(session: UserSession) {
  console.log('üÜï Criando proposta para usu√°rio:', session.userId);
  console.log('üìã Dados da sess√£o:', session.data);

  if (!session.userId) {
    throw new Error('Usu√°rio n√£o identificado');
  }

  let companyId = null;
  if (session.data.clientName) {
    console.log('üè¢ Criando empresa para o cliente:', session.data.clientName);

    const { data: company, error: companyError } = await supabase
      .from('companies')
      .insert({
        user_id: session.userId,
        name: session.data.clientName,
        email: session.data.clientEmail || null,
        phone: session.phone || null
      })
      .select()
      .single();

    console.log('üè¢ Resultado da cria√ß√£o da empresa:', { company, companyError });

    if (!companyError && company) {
      companyId = company.id;
    }
  }

  const proposalValue = session.data.value ?
    parseFloat(session.data.value.replace(/[^\d,]/g, '').replace(',', '.')) :
    null;

  console.log('üìù Dados da proposta a ser criada:', {
    user_id: session.userId,
    company_id: companyId,
    title: session.data.projectTitle || 'Proposta via Telegram',
    service_description: session.data.serviceDescription,
    detailed_description: session.data.detailedDescription,
    value: proposalValue,
    delivery_time: session.data.deliveryTime,
    observations: session.data.observations,
    template_id: 'moderno',
    status: 'rascunho'
  });

  const { data: proposal, error: proposalError } = await supabase
    .from('proposals')
    .insert({
      user_id: session.userId,
      company_id: companyId,
      title: session.data.projectTitle || 'Proposta via Telegram',
      service_description: session.data.serviceDescription,
      detailed_description: session.data.detailedDescription,
      value: proposalValue,
      delivery_time: session.data.deliveryTime,
      observations: session.data.observations,
      template_id: 'moderno',
      status: 'rascunho'
    })
    .select()
    .single();

  console.log('üìù Resultado da cria√ß√£o da proposta:', { proposal, proposalError });

  if (proposalError) {
    throw proposalError;
  }

  return proposal;
}

